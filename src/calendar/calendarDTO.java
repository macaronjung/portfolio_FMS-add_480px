package calendar;

public class calendarDTO {
	private int calendar_Num;
	private String calendar_T;
	private String calendar_S;
	private String calendar_E;
	
	
	public int getCalendar_Num() {
		return calendar_Num;
	}
	public void setCalendar_Num(int calendar_Num) {
		this.calendar_Num = calendar_Num;
	}
	public String getCalendar_T() {
		return calendar_T;
	}
	public void setCalendar_T(String calendar_T) {
		this.calendar_T = calendar_T;
	}
	public String getCalendar_S() {
		return calendar_S;
	}
	public void setCalendar_S(String calendar_S) {
		this.calendar_S = calendar_S;
	}
	public String getCalendar_E() {
		return calendar_E;
	}
	public void setCalendar_E(String calendar_E) {
		this.calendar_E = calendar_E;
	}
	
}
